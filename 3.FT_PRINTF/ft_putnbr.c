#include "ft_printf.h"

int	ft_putnbr(int nb)

{
	int	i;

	i = 0;
	if (nb == -2147483648)
	{
		ft_putstr("-2147483648");
		i++;
	}
	else
	{
		if (nb < 0)
		{
			nb = nb * -1;
			write (1, "-", 1);
			i++;
		}
		if (nb > 9)
			ft_putnbr(nb / 10);
		ft_putchar((nb % 10 + '0'));
	}
	i = i + ft_strlen(nb);
	return (i);
}
